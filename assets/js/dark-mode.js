$(document).ready(function(){
    $('input[type="checkbox"]').click(function(){
        if($(this).is(":checked")){
            $(".course-info").addClass("dark");
        }
        else {
            $(".course-info").removeClass("dark");
        }
        if($(this).is(":checked")){
            $("header").addClass("dark");
        }
        else{
            $("header").removeClass("dark");
        }
        if($(this).is(":checked")){
            $(".courses").addClass("dark");
        }
        else{
            $(".courses").removeClass("dark");
        }
        if($(this).is(":checked")){
            $(".discover").addClass("dark");
        }
        else{
            $(".discover").removeClass("dark");
        }
        if($(this).is(":checked")){
            $("#paid-course").addClass("dark");
        }
        else{
            $("#paid-course").removeClass("dark");
        }
        if($(this).is(":checked")){
            $(".team-area").addClass("dark");
        }
        else{
            $(".team-area").removeClass("dark");
        }
        if($(this).is(":checked")){
            $(".instructor").addClass("dark");
        }
        else{
            $(".instructor").removeClass("dark");
        }
        if($(this).is(":checked")){
            $("#up").addClass("dark");
        }
        else{
            $("#up").removeClass("dark");
        }
        if($(this).is(":checked")){
            $(".about").addClass("dark");
        }
        else{
            $(".about").removeClass("dark");
        }
        if($(this).is(":checked")){
            $(".contact").addClass("dark");
        }
        else{
            $(".contact").removeClass("dark");
        }
        if($(this).is(":checked")){
            $(".navbar-nav").addClass("dark");
        }
        else{
            $(".navbar-nav").removeClass("dark");
        }
        if($(this).is(":checked")){
            $(".navbar-toggler").addClass("dark");
        }
        else{
            $(".navbar-toggler").removeClass("dark");
        }
        if($(this).is(":checked")){
            $("#registr").addClass("dark");
        }
        else{
            $("#registr").removeClass("dark");
        }
        if($(this).is(":checked")){
            $("div.collapse navbar-collapse").addClass("dark");
        }
        else{
            $("div.collapse navbar-collapse").removeClass("dark");
        }
    });
});